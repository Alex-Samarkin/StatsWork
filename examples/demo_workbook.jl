using Revise
using StatsWorkbench
using DataFrames

#==============================================
Этот небольшой блок можно раскомментировать, если нужно быстро создать
игрушечный DataFrame и сохранить его в CSV. Сейчас основной пример ниже
использует уже подготовленный файл patients_3500.csv, чтобы показать работу
пакета на более реалистичном наборе медицинских переменных.

df = DataFrame(x=1:10, y=rand(10))
print(df)
save_data(df, "demo_data.csv")
===============================================#

# safe_load загружает данные из файла и мягко обрабатывает типичные ошибки
# чтения. В данном случае CSV превращается в DataFrame, который дальше будет
# зарегистрирован внутри workbook как один из источников переменных.
df = safe_load("./examples/patients_3500.csv")

# Workbook - это центральный контейнер анализа. Он хранит импортированные
# датасеты, вычисленные векторы, журнал операций и в будущем будет хранить
# результаты статистических процедур и отчетов.
wb = open_workbook("Demo")

# add_dataset! добавляет DataFrame в пространство переменных workbook.
# Имя :data становится именем источника. Все колонки df автоматически
# регистрируются как переменные: например :age, :height_cm, :sbp_mmhg.
add_dataset!(wb, :data, df)

# Добавим второй датасет, чтобы показать новую схему именования переменных.
# У него тоже есть колонка :id, поэтому имя :id становится неоднозначным:
# в vars(wb) оно будет показано как data.id и visits.id.
visits = DataFrame(id=1:5, visit_score=rand(5))
add_dataset!(wb, :visits, visits)

# vars(wb) возвращает единое пространство переменных. Если имя колонки
# уникально среди всех датасетов и вычисленных векторов, оно показывается
# коротко, например :age. Если имя конфликтует, используется квалификация
# dataset.variable, например Symbol("data.id") и Symbol("visits.id").
println(vars(wb))

# getvar достает переменную как вектор. Пользователь может работать с ней
# в стиле MATLAB: переменные анализа воспринимаются прежде всего как векторы,
# а не как колонки конкретного DataFrame.
println(getvar(wb, :age))

# Для неоднозначных имен используется явная квалификация через dataset.column.
# В Julia символ с точкой удобнее создавать через Symbol("data.id").
println(getvar(wb, Symbol("data.id")))

# calc! создает вычисленные переменные. Результаты не записываются сразу
# обратно в исходный DataFrame, а сохраняются как отдельные векторы внутри
# workspace. По умолчанию они попадают в пространство :calc.
#
# Здесь рост в сантиметрах делится на число 100. Число трактуется как скаляр
# и автоматически растягивается на длину вектора :height_cm.
calc!(wb, :height_m, :height_cm, /, 100)

# Операции между двумя переменными выполняются поэлементно. В этом примере
# рассчитывается пульсовое давление как разность систолического и
# диастолического давления.
calc!(wb, :pulse_pressure, :sbp_mmhg, -, :dbp_mmhg)

# Если длины векторов различаются, способ согласования задается параметром
# align. Здесь :age имеет длину основного датасета, а :visit_score содержит
# только 5 значений. Стратегия :truncate обрезает вычисление до минимальной
# длины. Информация о таком согласовании записывается в wb.logs.
calc!(wb, :age_plus_visit_score, :age, +, :visit_score; align=:truncate)

# После вычислений новые переменные появляются в общем пространстве имен.
# Если имя вычисленной переменной уникально, к нему можно обращаться коротко:
# :height_m, :pulse_pressure, :age_plus_visit_score.
println(vars(wb))
println(getvar(wb, :height_m))
println(getvar(wb, :pulse_pressure))
println(getvar(wb, :age_plus_visit_score))

# Журнал workbook фиксирует важные действия: созданные переменные, формулы
# вычислений и случаи автоматического согласования длины векторов.
println(wb.logs)

# commit! явно возвращает вычисленный вектор в выбранный датасет. Это сделано
# намеренно: вычисления в workspace не портят исходные данные автоматически.
#
# Поскольку :age_plus_visit_score короче, чем датасет :data, при записи назад
# нужно указать стратегию align. :padmissing дополняет недостающие строки
# значениями missing, чтобы колонка стала совместима с числом строк DataFrame.
commit!(wb, :age_plus_visit_score; to=:data, align=:padmissing)

# save_workspace сохраняет все состояние анализа, а не только один датасет:
# workbook, импортированные данные, вычисленные векторы, журнал операций и
# связи переменных. Это отличается от save_data, который сохраняет отдельный
# DataFrame в CSV/XLSX/JLD2/Parquet.
save_workspace(wb, "demo_workspace.jld2")

# load_workspace восстанавливает workbook. После загрузки можно продолжать
# работать с теми же переменными. Здесь проверяем колонку, которую commit!
# добавил в исходный датасет :data.
restored = load_workspace("demo_workspace.jld2")
println(getvar(restored, Symbol("data.age_plus_visit_score")))

# close_workbook! помечает workbook как закрытый. Сейчас это минимальный флаг,
# но в дальнейшем вокруг него можно построить финализацию анализа, сбор логов,
# проверку несохраненных результатов и генерацию итогового отчета.
close_workbook!(wb)
